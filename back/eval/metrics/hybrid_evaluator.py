FAITHFULNESS_PROMPT = """Evaluate the faithfulness of the generated answer compared to the retrieved context.

Faithfulness measures whether the generated answer can be inferred from the retrieved context
without introducing external information or hallucinations.

**Question:** {question}

**Retrieved Context:** 
{context}

**Generated Answer:**
{answer}

**Evaluation Criteria:**
1. Can all claims in the answer be directly inferred from the context?
2. Does the answer introduce any information not present in the context?
3. Are there any contradictions between the answer and the context?

Rate faithfulness from 0 to 1, where:
- 1.0 = All claims are directly supported by the context
- 0.5 = Some claims are supported, others are assumptions
- 0.0 = Answer contains significant hallucinations or contradictions

Output format: JSON with "score" (0-1) and "explanation" fields.
"""

